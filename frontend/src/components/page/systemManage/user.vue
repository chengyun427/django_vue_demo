    setDepartment() {
      this.defautChoose = [];
      if (this.editInfoBefore) {
        if (1 == this.selectId.length) {
          this.getDeptData();//获取部门树
          this.getHasUseDept();//获取已授权的部门
          this.titleUserName = this.editInfoBefore.userName;
          // this.defautChoose=[this.editInfoBefore.deptCode];//默认选中当前所属部门
          // this.chooseDept.id=this.editInfoBefore.deptCode;
          this.dialogDeptSet = true;
        } else {
          this.$message({
            showClose: true,
            message: "请选择一条数据进行设置部门",
            type: 'error'
          });
        }
      } else {
        this.$message({
          showClose: true,
          message: "请选择一条数据进行设置部门",
          type: 'error'
        });
      }
    },
    //确认设置部门
    setDeptSure() {
      if (0 < this.$refs.tree2.getCheckedKeys().length) {
        var params = new URLSearchParams();
        params.append('userUuid', this.editInfoBefore.userUuid);
        params.append('deptList', this.chooseDept.id);
        this.openFullScreen();//打开遮罩
        this.$axios.post(this.zpUserdept, params).then(res => {
          this.loading.close(); //关闭加载
          if (0 == res.data.code) {//设置成功
            this.$message({
              showClose: true,
              message: res.data.msg,
              type: 'success'
            });
            this.getData();
            this.dialogDeptSet = false;//设置部门dilog关闭
          } else {
            this.$message({
              showClose: true,
              message: res.data.msg,
              type: 'error'
            });
          }
        })
      } else {
        this.$message({
          showClose: true,
          message: '请选择一个部门',
          type: 'error'
        });
      }

    },
    //选择部门-设置部门
    handleNodeClick2(data) {
      this.$refs.tree2.setCheckedKeys([data.id]);
      this.chooseDept = data;
    },
    //选择部门-设置部门-check
    chooseOneDept(data, checked) {
      if (true == checked) {
        this.$refs.tree2.setCheckedKeys([]);
        this.handleNodeClick2(data);
      }
    },
    //选择部门-修改信息
    handleNodeClick1(data) {
      this.$refs.tree1.setCheckedKeys([data.id]);
      this.chooseDept = data;
    },
    //选择部门-修改信息-check
    chooseOneDept1(data, checked) {
      if (true == checked) {
        this.$refs.tree1.setCheckedKeys([]);
        this.handleNodeClick1(data);
      }
    },
    //每页条数
    handleSizeChange(val) {
      this.pageSize = val;
      this.getData();
    },
    //第几页
    handleCurrentChange(val) {
      this.pageNum = val;
      this.getData();
    },
    //每页条数
    handleSizeChangePost(val) {
      this.pageSizePost = val;
      this.getPostByDept();
    },
    //第几页
    handleCurrentChangePost(val) {
      this.pageNumPost = val;
      this.getPostByDept();
    },
    //选中checkbox，取消选中 此处返回的是以选中的val
    chooseOne(val) {
      var userUuid = [];
      for (var i = 0; i < val.length; i++) {
        userUuid.push(val[i].userUuid)
      }
      this.delNum = userUuid.length;
      this.selectId = userUuid;//将选中的id存起来
      if (1 == val.length) {//选中一条时将选中的值存起来 否则清空待修改
        this.editInfoBefore = JSON.parse(JSON.stringify(val[0]));
        this.getUserByUserUuid(JSON.parse(JSON.stringify(val[0])).userUuid);
      } else {
        this.editInfo = {};
        this.editInfoBefore = {};
      }
    },
    //全选
    chooseAll(val) {
      var userUuid = [];
      for (var i = 0; i < val.length; i++) {
        userUuid.push(val[i].userUuid)
      }
      this.delNum = userUuid.length;
      this.selectId = userUuid;//将选中的id存起来
      this.editInfo = {};
      this.editInfoBefore = {};
    },
    getData() {
      this.openFullScreen();//打开遮罩
      // var params = new URLSearchParams();
      var params = {
        'userName': this.formInline.userName,
        'userAccount': this.formInline.userAccount,
        'userGender': this.formInline.userGender
      };
      this.$axios.get(this.getDataUrl + '?pageSize=' + this.pageSize + '&pageNum=' + this.pageNum, params).then(res => {
        this.loading.close(); //关闭加载
        this.editInfoBefore = {};
        this.selectId = {};
        if (0 == res.data.code) {
          this.tableData = res.data.data.list;
          this.dataSize = res.data.data.totalRecords;
        } else {
          this.$message({
            showClose: true,
            message: res.data.msg,
            type: 'error'
          });
        }
      });
    },
    getUserByUserUuid(userUuid) {
      var params = new URLSearchParams();
      params.append('userUuid', userUuid);
      this.$axios.post(this.getUserByUserUuidUrl, params).then(res => {
        this.editInfoBefore = {};
        if (0 == res.data.code) {//查询成功
          this.editInfoBefore = res.data.data;
        } else {
          this.$message({
            showClose: true,
            message: res.data.msg,
            type: 'error'
          });
        }
      })
    },
    //时间格式化
    dateFormat(row, column) {
      var date = row[column.property];
      if (date == undefined) {
        return "";
      }
      return this.$moment(date).format("YYYY-MM-DD HH:mm:ss");
    }
  },
};
